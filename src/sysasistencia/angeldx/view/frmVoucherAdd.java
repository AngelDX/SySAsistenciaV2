    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        try{
            String evento[]=((String)cboEvento.getSelectedItem()).split("-");
            data.setEvento_id(Integer.parseInt(evento[0]));
            data.setEstudiante_id(Integer.parseInt(txtId.getText()));
            data.setNumero(txtNumero.getText());
            data.setImporte(Double.parseDouble(txtImporte.getText()));
            data.setTipo((String) cboTipo.getSelectedItem());
            if(voucher.registarVoucher(data)){
                JOptionPane.showMessageDialog(rootPane,"Datos registrados");
                dispose();
            }else{
                JOptionPane.showMessageDialog(rootPane,"Error: Los Datos no se registraron");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

