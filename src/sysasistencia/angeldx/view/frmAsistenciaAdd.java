    private void txtCodigoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCodigoKeyReleased
        // TODO add your handling code here:
        String tipo="";
        if(evt.getKeyCode()==10){//SI SE PRESIONA ENTER
          if(txtCodigo.getText().length()==9){ 
            est=control.buscarEstudiante(txtCodigo.getText()); 
           if(est.getId()!=0){ //Estudiante esta registrado en la base de datos               
            lblNombre.setText(est.getNombre());
            lblCodigo.setText(est.getCodigo());
            lblCarrera1.setText(Funciones.carreraFull(est.getCarrera()));
            String evento[]=((String)cboEvento.getSelectedItem()).split("-"); 
            if(optEntrada.isSelected()){
                tipo="ENTRADA";
            }
            if(optSalida.isSelected()){
                tipo="SALIDA";
            }
            //Verificar si ya ingreso al evento para no registrar nuevamente su ingreso
                if(control.verificarRegistro(est.getId(),Integer.parseInt(evento[0]),tipo)){
                    lblMensaje.setText("Su ingreso esta Registrado anteriormente");
                    JOptionPane.showMessageDialog(null,"¡Su ingreso esta Registrado anteriormente!");
                    btnRegistar.setEnabled(false);
                }else{
                    //lblMensaje.setText("Bienvenido, Presione el boton Registrar"); 
                    //btnRegistar.setEnabled(true);
                    //String evento[]=((String)cboEvento.getSelectedItem()).split("-");
                    data.setEstudiante_id(est.getId());
                    data.setEvento_id(Integer.parseInt(evento[0]));
                    data.setFechahora(año+"-"+mes+"-"+dia+" "+hora24+":"+minutos+":"+segundos);
                    if(optEntrada.isSelected()){
                        data.setTipo("ENTRADA");
                    }
                    if(optSalida.isSelected()){
                        data.setTipo("SALIDA");
                    }

                    if(control.registarControl(data)){
                        //JOptionPane.showMessageDialog(null,"Asistencia registrada"); 
                        lblMensaje.setText("Asistencia registrada");
                        btnRegistar.setEnabled(false);
                    }else{
                        JOptionPane.showMessageDialog(null,"No se registro la Asistencia");
                    }
                        txtCodigo.requestFocus();
                        //lblCarrera1.setText("");
                        //lblCodigo.setText("");
                        //lblMensaje.setText("");
                        //lblNombre.setText("");
                }            
           }else{
            lblNombre.setText("Estudiante no encontrado...");
           }
           txtCodigo.setText("");
           txtCodigo.requestFocus();
          }else{
            part=control.buscarParticipante(txtCodigo.getText());
            if(part.getId()!=0){ //Participante esta registrado en la base de datos               
            lblNombre.setText(part.getNombre());
            lblCodigo.setText(String.valueOf(part.getId()));
            lblCarrera1.setText(part.getEntidad());
            String evento[]=((String)cboEvento.getSelectedItem()).split("-"); 
            if(optEntrada.isSelected()){
                tipo="ENTRADA";
            }
            if(optSalida.isSelected()){
                tipo="SALIDA";
            }
            //Verificar si ya ingreso al evento para no registrar nuevamente su ingreso
                if(control.verificarRegistroPart(part.getId(),Integer.parseInt(evento[0]),tipo)){
                    lblMensaje.setText("Su ingreso esta Registrado anteriormente");
                    JOptionPane.showMessageDialog(null,"¡Su ingreso esta Registrado anteriormente!");
                    btnRegistar.setEnabled(false);
                }else{                    
                    data.setParticipante_id(part.getId());
                    data.setEvento_id(Integer.parseInt(evento[0]));
                    data.setFechahora(año+"-"+mes+"-"+dia+" "+hora24+":"+minutos+":"+segundos);
                    if(optEntrada.isSelected()){
                        data.setTipo("ENTRADA");
                    }
                    if(optSalida.isSelected()){
                        data.setTipo("SALIDA");
                    }

                    if(control.registarControlPart(data)){
                        //JOptionPane.showMessageDialog(null,"Asistencia registrada"); 
                        lblMensaje.setText("Asistencia registrada");
                        btnRegistar.setEnabled(false);
                    }else{
                        JOptionPane.showMessageDialog(null,"No se registro la Asistencia");
                    }
                        txtCodigo.requestFocus();
                        //lblCarrera1.setText("");
                        //lblCodigo.setText("");
                        //lblMensaje.setText("");
                        //lblNombre.setText("");
                }            
           }else{
            lblNombre.setText("Participante no encontrado...");
           }
           txtCodigo.setText("");
           txtCodigo.requestFocus();
          }
        }
        
    }//GEN-LAST:event_txtCodigoKeyReleased

