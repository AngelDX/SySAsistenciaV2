    void bloquear(){
        txtCod.setEnabled(false);
        txtNom.setEnabled(false);
        
        cboCarrera.setEnabled(false);
        cboCiclo.setEnabled(false);
        cboGrupo.setEnabled(false);
        
        btnGuardar.setEnabled(false);
        btnCancelar.setEnabled(false);
    }
    
    void desbloquear(){
        txtCod.setEnabled(true);
        txtNom.setEnabled(true);
        
        cboCarrera.setEnabled(true);
        cboCiclo.setEnabled(true);
        cboGrupo.setEnabled(true);
        
        btnGuardar.setEnabled(true);
        btnCancelar.setEnabled(true);  
    }
    
    void limpiar(){
        txtCod.setText("");
        txtNom.setText("");
        
        cargarCarreras();
        cargarCiclo();
        cargarGrupo();
        
        txtId.setText("");
    }
    
    private void cargarVoucherId() {
        try {
            ArrayList<VoucherTO> data=new ArrayList();
            id=jTable1.getValueAt(jTable1.getSelectedRow(), 0);
            data=voucher.listarVoucherId((int) id);
            DefaultTableModel dtm=(DefaultTableModel) jTable2.getModel();
            dtm.setNumRows(0);
            
            for(VoucherTO x:data){
                dtm.addRow(new Object[]{
                    x.getEventoData().getDescripcion(),
                    x.getNumero(),
                    x.getImporte(),
                    x.getTipo()
                }); 
            }      
            jTable2.setModel(dtm);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
    }
