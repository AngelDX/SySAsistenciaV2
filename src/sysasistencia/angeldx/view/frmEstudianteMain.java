    private void cargarEstudiantes(String codigo,String nombre) {
        try {
            ArrayList<EstudianteTO> est=new ArrayList();
            est=estudiante.listar(codigo,nombre);
            DefaultTableModel dtm=(DefaultTableModel) jTable1.getModel();
            dtm.setNumRows(0);
            int i=0;
            for(EstudianteTO x:est){
                dtm.addRow(new Object[]{
                    x.getId(),
                    x.getCodigo(),
                    x.getNombre(),
                    Funciones.carreraFull(x.getCarrera()),
                    x.getCiclo(),
                    x.getGrupo()
                }); 
                i++;
            }      
            jTable1.setModel(dtm);
            txtTotal.setText(String.valueOf(i));
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
    }

    private void cargarCarreras() {
        try {
            ArrayList<EstudianteTO> data=new ArrayList();    
            data=estudiante.listarCarreras();
            for(EstudianteTO x : data){
                if(x.getCarrera()!=null){
                    cboCarrera.addItem(x.getCarrera());
                }
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex);
        }
    }
    
